package at.droll.div2builder.framework.inventory;

/**
 * Enum of all available inventory slots
 * @author Marco Droll
 */
public enum InventorySlot {
	/**
	 * Armor or chest
	 */
	ARMOR,
	
	/**
	 * Backpack
	 */
	BACKPACK,
	
	/**
	 * Holster
	 */
	HOLSTER,
	
	/**
	 * Kneepad
	 */
	KNEEPAD,
	
	/**
	 * Glove
	 */
	GLOVE,
	
	/**
	 * Mask
	 */
	MASK,
	
	/**
	 * Primary weapon
	 */
	PRIMARY,
	
	/**
	 * Secondary weapon
	 */
	SECONDARY,
	
	/**
	 * Pistol sidearm
	 */
	PISTOL
}
